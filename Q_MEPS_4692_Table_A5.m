function Q_MEPS = Q_MEPS_4692_Table_A5(V)
% FUNCTION NAME:
%   Q_MEPS_4692_Table_A5
%
% DESCRIPTION:
% Compute the maximum allowable hot water cylinder heat losses (MEPS losses)
% from standard AS/NZS 4692.2:2005 according to Appendix A5 and Table A5.
%
% The temperature rise is 55 K.
%
% The heat loss calculation is performed according to Table A5,
% with the following extra notes:
% * loss allowance for TPR valves is not included. This allowance is fixed
%   at 0.12 kWh.day-1 for any non-zero number of TPR valves.
%
% INPUT:
%   V - Hot water cylinder storage capacity (m3).
%
% OUTPUT:
%   Q_MEPS - Maximum allowed losses that are allowed for the hot water cylinder
%      with storage volume V (kWh.day-1).
%
% ASSUMPTIONS AND LIMITATIONS:
%   This function has not been vectorised. Only one argument may be a vector.
%
%
% REVISION HISTORY:
%   20210709 - bjem
%       * Initial implementation
Q_table =[...
0.0000, 0.55; ...
0.0119, 0.55; ...
0.0238, 0.65; ...
0.0357, 0.75; ...
0.0476, 0.85; ...
0.0595, 0.95; ...
