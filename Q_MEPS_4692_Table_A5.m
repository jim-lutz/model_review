0.2042, 1.74; ...
0.2250, 1.84; ...
0.2458, 1.94; ...
0.2667, 2.04; ...
0.2875, 2.14; ...
0.3083, 2.24; ...
0.3292, 2.34; ...
0.3500, 2.43; ...
0.3708, 2.53; ...
0.3917, 2.63; ...
0.4125, 2.73; ...
0.4333, 2.83; ...
0.4542, 2.93; ...
0.4750, 3.03; ...
